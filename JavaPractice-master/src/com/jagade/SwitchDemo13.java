package com.jagade;

public class SwitchDemo13 {

	public static void main(String[]args) {
		
		int choice = 3 ;
		
		switch(choice) {
		
		case 1: System.out.println("In Case- 1");
		break ;
		
		case 2: System.out.println("In Case- 2");
		break ;
		
		case 3: System.out.println("In Case- 3");
		break ;
		
		default : System.out.println("Invalid");
		}
	}
}
